# Dictionary in python
# Common methods can be used with dictionary includes keys, values, items, find length in dictionary

DictyTutorial = {"K1":"Val1", 2:"Val2", "K3": 2025}
print(DictyTutorial.keys()) #print all the keys
print(DictyTutorial.values()) #print all the values
print(DictyTutorial.items()) #print all key-value pairs
print(len(DictyTutorial)) #print the number of pairs