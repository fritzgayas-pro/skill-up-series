# Standard types in Python are built-in data types for representing values.
# Numeric types: int (whole numbers), float (decimals), complex (real & imaginary).
# Sequence types: str (text), list (ordered & mutable), tuple (ordered & immutable).
# dict: key-value mappings.
# bool: logical True/False.
# NoneType: represents absence of a value.

# Numeric Standard Type
score = 90
percentage = 98.25

# String Standard Type
user_message = "Hello Fritz!"
user_message.upper()

# List 
li = [43,58,98.25, "Hello World"]

# Tuple
tu = (43,58,98.25, "Hello World")

# Dictionairy
di = {"A":"val1", "B":"val2"}
